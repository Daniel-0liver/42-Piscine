#include <unistd.h>
int main(int	argc,char	**argv)
{
	int		n;
	(void)	argc;
	(void)	argv;

	n = 0;
	while (argv[0][n] != '\0')
	{
		write(1, &argv[0][n], 1);
		n++;
	}
	return (0);
}
